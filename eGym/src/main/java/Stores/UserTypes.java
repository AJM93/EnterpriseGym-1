package Stores;

/**
 *
 * @author Dreads
 */
public class UserTypes {
    private int Id;
    private String type;
    
    public UserTypes(final int Id, final String type)
    {
        this.Id = Id;
        this.type = type;
    }
    /**
     * @return the Id
     */
    public int getId() {
        return Id;
    }

    /**
     * @param Id the Id to set
     */
    public void setId(int Id) {
        this.Id = Id;
    }

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }
}
