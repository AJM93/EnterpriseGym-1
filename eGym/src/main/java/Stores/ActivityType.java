package Stores;

/**
 *
 * @author Tom
 */
public class ActivityType {
    private int ID;
    private String type;
    
    public ActivityType (int id, String type){
        this.ID = id;
        this.type = type;
    }

    /**
     * @return the ID
     */
    public int getID() {
        return ID;
    }

    /**
     * @param ID the ID to set
     */
    public void setID(int ID) {
        this.ID = ID;
    }

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }
}
